// Set the core image link
var imgLink = "https://picsum.photos/600/600/?image=";

// Button click events
$('#new-quote').on('click', function(){
  genQuote();
});

$('#new-image').on('click', function(){
  genImage();
});

$('#new-post').on('click', function(){
  genQuote();
  genImage();
});

// Pick and display a new quote from the arrays above
function genQuote() {
  var ranNum = Math.floor(Math.random() * (quotes.length));
  document.getElementById('quote-display').innerHTML = '"' + quotes[ranNum] + '"';
}

// Choose a random image number and pass it to isThis404
function genImage() {
  var ranNum = Math.floor(Math.random() * (1080));
  isThis404(ranNum);
}

// Checks if the image exists or not and either returns the image by displaying it or retrys the genImage function with a new image
function isThis404(imgNum) {
  $.ajax({
    url: imgLink + imgNum,
    success: function () {
      $('.inner-container').css('background', 'url(' + imgLink + imgNum + ')');
    },
    error: function () {
      genImage();
    }
  });
};
